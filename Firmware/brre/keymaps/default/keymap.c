enum layers {
    _BASE,
    _FIRST,
    _SECOND
};

uint8_t get_toggle_switch_layer(void) {
        gpio_set_pin_input_high(GP16);
        gpio_set_pin_input_high(GP17);
    if (gpio_read_pin(GP16) && !gpio_read_pin(GP17)) {
        return _FIRST;
    } else if (!gpio_read_pin(GP16) && gpio_read_pin(GP17)) {
        return _SECOND;
    } else {
        return _BASE;
    }
}

void handle_toggle_switch_layer(void) {
    uint8_t switch_state = get_toggle_switch_layer();
    switch (switch_state) {
        case _BASE:
            layer_on(_BASE);
            layer_off(_FIRST);
            layer_off(_SECOND);
            break;
        case _FIRST:
            layer_off(_BASE);
            layer_on(_FIRST);
            layer_off(_SECOND);
            break;
        case _SECOND:
            layer_off(_BASE);
            layer_off(_FIRST);
            layer_on(_SECOND);
            break;
    }
}

